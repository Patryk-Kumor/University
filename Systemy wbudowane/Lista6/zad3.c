// inicjalizacja SPI
void spi_init()
{
    // ustaw piny MOSI, SCK i ~SS jako wyjścia
    DDRB |= _BV(DDB3) | _BV(DDB5) | _BV(DDB2);
    // PB2 jako wyjścia (
    DDRB |= _BV(PB2);
    // włącz SPI w trybie master z zegarem 250 kHz
    SPCR = _BV(SPE) | _BV(MSTR) | _BV(SPR1);
}

// transfer jednego bajtu
uint8_t spi_transfer(uint8_t data)
{
    // rozpocznij transmisję
    SPDR = data;
    // czekaj na ukończenie transmisji
    while (!(SPSR & _BV(SPIF)));
    // wyczyść flagę przerwania
    SPSR |= _BV(SPIF);
    // zwróć otrzymane dane
    return SPDR;
}

int main()
{
  // zainicjalizuj SPI
  spi_init();
  uint16_t i; uint8_t c; uint8_t x; uint8_t y;
  while(1) {
    for (i = 0; i < audio_len; i++) {
      c = (uint8_t) pgm_read_word(&audio[i]);
      x = (c >> 4) | 0b01110000 ;
      y = c << 4;
      PORTB &= ~_BV(PB2); // stan niski CS
      spi_transfer(x);
      spi_transfer(y);
      PORTB |= _BV(PB2); // stan wysoki CS 
      _delay_us(120);
    } 
  }
}

